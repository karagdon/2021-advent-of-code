// given shape of [{direction: string, distance: integer},...], determine the product of total hDistance and vDistasnce
const getProductOfHorizontalAndVerticalDepths = (arr) => {
  let hDistance =0;
  let vDistance = 0;
  for(let i = 0; i < arr.length; i++) {
   if (arr[i].direction === "forward") {
     hDistance = hDistance + arr[i].distance;
   }
   if (arr[i].direction === "down") {
     vDistance = vDistance + arr[i].distance;
   }
   if (arr[i].direction === "up") {
     vDistance = vDistance - arr[i].distance;
   }
  }
  return hDistance * vDistance;
}


