  for(let i = 0; i < arr.length; i++) {
    
    if (arr[i].direction === "down") {
      //down X increases your aim by X units.
      aim = aim + arr[i].distance;
    }
    if (arr[i].direction === "up") {
      // up X decreases your aim by X units.

      aim = aim - arr[i].distance;
    }
    if (arr[i].direction === "forward") {
      //forward X does two things:
      //   It increases your horizontal position by X units.
      hDistance2 = hDistance2 + arr[i].distance;
      
      //    It increases your depth by your aim multiplied by X.
      vDistance2 = vDistance2 + (aim*arr[i].distance);
    }

    console.log("h:" +hDistance2+ " d:"+vDistance2+ " aim:" + aim)
  }
  return vDistance2*hDistance2;
